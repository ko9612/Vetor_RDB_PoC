// vectorEngine.ts — 【의미 검색 엔진】 (화면 왼쪽 패널)
//
// ┌─ 이 엔진이 하는 일 ─────────────────────────────────────────────┐
// │  "설비가 멈출 것 같아요" 라고 입력해도
// │  "라인 정지 위험" 이라고 적힌 과거 수주를 찾아낸다.
// │
// │  비결: 단어를 글자 그대로 비교하는 게 아니라,
// │  AI가 문장의 '뜻'을 숫자 좌표(벡터)로 변환한 뒤
// │  가장 '방향이 가까운' 좌표를 가진 수주를 찾는다.
// │  → 단어가 달라도 뜻이 비슷하면 검색된다.
// └───────────────────────────────────────────────────────────────┘
//
// ┌─ RDB 엔진(rdbEngine)과의 결정적 차이 ─────────────────────────────┐
// │  RDB  : "긴급"이라는 글자가 DB에 있어야만 찾는다 (글자 검색)
// │  벡터 : "빨리 보내야 해요" → "긴급 납기" 와 같은 의미임을
// │         AI가 이해하고 찾는다 (의미 검색)
// │
// │  예시(S1 시나리오):
// │    질의: "설비가 멈출 위험, 빨리 보내야"
// │    RDB 결과: 0건 (DB에 저 단어들이 없음)
// │    벡터 결과: SO-001, SO-016 탐지 ✓ (뜻이 같은 것을 인식)
// └────────────────────────────────────────────────────────────────┘
//
// ┌─ 처리 흐름 (검색 1회 기준) ────────────────────────────────────────┐
// │  [1] 질의문  →  AI가 뜻을 숫자 4096개짜리 좌표로 변환
// │  [2] 좌표    →  Pinecone에서 가장 가까운 좌표의 수주 TOP-K 검색
// │  [3] 결과    →  "왜 비슷한가" 근거 태그 부여 (화면 배지로 표시)
// │  [4] 결과    →  AI에게 건네 "대응방안 글" 작성 요청
// │  ※ 어디서든 오류가 나면 조용히 error 상태를 반환 (실패 격리)
// └─────────────────────────────────────────────────────────────────┘
// ════════════════════════════════════════════════════════════════

import type { PanelResult } from "@scm/shared";
import type { SolarClient } from "../solarClient.js";
import { searchVector } from "../stores/pinecone.js";

export async function runVector(
  query: string,
  topK: number,
  solar: SolarClient
): Promise<PanelResult> {
  const t0 = Date.now(); // 소요시간 측정용 시작 시각

  try {
    // ── [1] 질의 임베딩 ──────────────────────────────────────────
    // 사람이 입력한 문장을 AI가 이해하는 숫자 좌표(4096차원 벡터)로 변환한다.
    // "query" 모델 사용 — 검색어에 최적화된 변환 방식
    // (저장된 수주는 "passage" 모델로 변환했기 때문에 종류를 맞춰야 검색 정확도가 높다)
    const qVector = await solar.embed(query, "query");

    // ── [2] Pinecone 유사 수주 검색 ──────────────────────────────
    // 방금 만든 질의 좌표와 가장 '방향이 가까운' 수주를 Pinecone에서 찾는다.
    // 유사도 계산 방식은 코사인(cosine) — 0~1 사이 값으로, 1에 가까울수록 유사.
    // topK: 상위 몇 건까지 가져올지 (기본값 3)
    const hits = await searchVector(qVector, topK);

    // ── [3] 결과 없음 처리 ───────────────────────────────────────
    // 유사한 수주가 하나도 없는 경우 (거의 없지만 안전하게 처리)
    if (hits.length === 0) {
      return {
        status: "empty",
        engine: "vector",
        tookMs: Date.now() - t0,
        hits: [],
        advice: null,
        reason: "벡터 검색 결과가 없습니다.",
      };
    }

    // ── [4] 근거 태그 부여 ───────────────────────────────────────
    // 각 검색 결과에 "왜 이 수주가 찾혔는가"를 표시하는 배지를 붙인다.
    // 벡터 검색은 글자가 아닌 의미로 찾기 때문에, 근거는 코사인 유사도 점수로 표현한다.
    // 예: ["의미 유사", "cosine: 0.87"] → 화면 배지로 표시됨
    for (const hit of hits) {
      hit.evidence = [`의미 유사`, `cosine: ${hit.score.toFixed(2)}`];
    }

    // ── [5] AI 대응방안 생성 ─────────────────────────────────────
    // 찾은 과거 수주들을 근거 자료로 삼아, AI(Solar LLM)에게 대응방안 글을 써달라고 요청한다.
    // 근거 없이 AI가 지어내는 것을 막기 위해, 반드시 검색 결과가 있을 때만 호출한다.
    const advice = await solar.recommend(query, hits);

    return {
      status: "success",
      engine: "vector",
      tookMs: Date.now() - t0,
      hits,
      advice,
    };
  } catch (err) {
    // ── 실패 격리 ────────────────────────────────────────────────
    // 이 엔진에서 어떤 오류가 발생해도 error 상태를 반환할 뿐,
    // 오른쪽 RDB 패널에는 전혀 영향을 주지 않는다.
    return {
      status: "error",
      engine: "vector",
      tookMs: Date.now() - t0,
      hits: [],
      advice: null,
      reason: err instanceof Error ? err.message : String(err),
    };
  }
}
