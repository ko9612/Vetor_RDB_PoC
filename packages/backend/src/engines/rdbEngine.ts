// ════════════════════════════════════════════════════════════════
// rdbEngine.ts — 【키워드 검색 엔진】 (화면 오른쪽 패널)
//
// ┌─ 이 엔진이 하는 일 ─────────────────────────────────────────────┐
// │  입력된 문장에서 핵심 단어를 뽑아,                               
// │  그 단어가 실제로 기록된 과거 수주를 찾는다.                       
// │                                                             
// │  비결 없이 단순하다: 단어가 DB 안에 그대로 있어야만 찾는다.         
// │  → 전통적인 검색 엔진(구글 초기, 도서관 색인 카드)과 동일.         
// └───────────────────────────────────────────────────────────────┘
//
// ┌─ 벡터 엔진(vectorEngine)과의 결정적 차이 ─────────────────────────┐
// │  벡터 : 뜻이 같으면 찾는다  ("긴급" ≈ "빨리 보내야")
// │  RDB  : 글자가 같아야 찾는다 ("긴급" ≠ "빨리 보내야")
// │
// │  이 차이가 이 PoC가 증명하려는 핵심이다.                      
// │
// │  예시(S1 시나리오):
// │    질의: "설비가 멈출 위험, 빨리 보내야"
// │    → DB에 "설비", "멈출", "빨리" 같은 단어가 없어서 결과 0건
// │    → "검색 결과 없음" 표시, AI 추천 없음                        
// │  예시(S2 시나리오):
// │    질의: "냉동·냉장 상태로 차갑게 운반"
// │    → "냉동", "냉장" 단어가 DB에 있어서 일부 수주 검색됨 (부분)
// └────────────────────────────────────────────────────────────────┘
//
// ┌─ 처리 흐름 (검색 1회 기준) ────────────────────────────────────────┐
// │  [1] 질의문  →  핵심 단어(키워드) 추출 (조사·불용어 제거)
// │  [2] 단어들  →  SQLite에서 그 단어가 들어있는 수주 검색 
// │  [3] 결과 0건이면 → AI 안 부르고 바로 "결과 없음" 반환
// │       (근거도 없는데 AI가 지어내는 '환각'을 막기 위함)
// │  [4] 결과 있으면 → "어떤 단어로 찾혔는지" 근거 태그 부여
// │  [5] 결과    →  AI에게 건네 "대응방안 글" 작성 요청
// │  ※ 어디서든 오류가 나면 조용히 error 상태를 반환 (실패 격리)
// └─────────────────────────────────────────────────────────────────┘
// ════════════════════════════════════════════════════════════════

import type { PanelResult } from "@scm/shared";
import type { SolarClient } from "../solarClient.js";
import { searchFts } from "../stores/sqlite.js";

export async function runRdb(
  query: string,
  topK: number,
  solar: SolarClient
): Promise<PanelResult> {
  const t0 = Date.now(); // 소요시간 측정용 시작 시각

  try {
    // ── [1] 키워드 추출 + SQLite 검색 ───────────────────────────
    // 질의문에서 "검색에 쓸 단어"를 추출한 뒤, 그 단어가 들어있는 수주를 찾는다.
    // 예: "냉동 냉장 상태로 차갑게" → ["냉동", "냉장", "상태로", "차갑게"]
    //   → DB에 "냉동" OR "냉장" 이 들어있는 수주만 반환
    // keywords: 추출된 단어 목록 (화면에 "검색 키워드" 배지로 표시됨)
    // hits: 단어가 실제로 들어있는 수주 목록 (없으면 빈 배열)
    const { keywords, hits } = searchFts(query, topK);

    // ── [2] 결과 없음 처리 (환각 방지) ──────────────────────────
    // 한 건도 못 찾으면 여기서 즉시 종료하고 AI를 부르지 않는다.
    // 이유: 근거 자료가 없는 상태에서 AI를 부르면 없는 수주를 만들어낼 수 있다.
    //       (이것이 S1·S4·S5 시나리오에서 RDB가 "결과 없음"을 표시하는 이유)
    if (hits.length === 0) {
      return {
        status: "empty",
        engine: "rdb",
        tookMs: Date.now() - t0,
        hits: [],
        advice: null,
        keywords,
        reason: "추출 키워드가 과거 수주 텍스트와 일치하지 않습니다.",
      };
    }

    // ── [3] 근거 태그 부여 ───────────────────────────────────────
    // 각 검색 결과에 "이 단어 때문에 찾혔다"는 배지를 붙인다.
    // RDB 검색은 글자 일치로 찾기 때문에, 근거는 실제 일치한 단어들이다.
    // 예: ["키워드: 냉동", "키워드: 냉장"] → 화면 배지로 표시됨
    for (const hit of hits) {
      hit.evidence = keywords.map((kw) => `키워드: ${kw}`);
    }

    // ── [4] AI 대응방안 생성 ─────────────────────────────────────
    // 찾은 과거 수주들을 근거 자료로 삼아, AI(Solar LLM)에게 대응방안 글을 써달라고 요청한다.
    // 벡터 엔진과 동일하지만, 이쪽은 "글자로 찾은 수주"가 근거가 된다.
    const advice = await solar.recommend(query, hits);

    return {
      status: "success",
      engine: "rdb",
      tookMs: Date.now() - t0,
      hits,
      advice,
      keywords,
    };
  } catch (err) {
    // ── 실패 격리 ────────────────────────────────────────────────
    // 이 엔진에서 어떤 오류가 발생해도 error 상태를 반환할 뿐,
    // 왼쪽 벡터 패널에는 전혀 영향을 주지 않는다.
    return {
      status: "error",
      engine: "rdb",
      tookMs: Date.now() - t0,
      hits: [],
      advice: null,
      reason: err instanceof Error ? err.message : String(err),
    };
  }
}
